//! Direct libllama C API backend (Phase 2).
//!
//! Wraps the real `llama.cpp` library in-process — no subprocess overhead,
//! real `llama_perf_context` throughput stats, and a context pool for true
//! per-request concurrency.
//!
//! # Requirements
//!
//! - `brew install llama.cpp` (installs `libllama.dylib` + `llama.h`)
//! - Enable the feature: `cargo build --features ax-serving-engine/libllama`
//! - Or set `AXS_LLAMA_INCLUDE_DIR` / `AXS_LLAMA_LIB_DIR` for custom paths.
//!
//! # Environment variables
//!
//! | Variable | Default | Description |
//! |---|---|---|
//! | `AXS_LIBLLAMA_N_GPU_LAYERS` | `99` (all) | GPU offload layers |
//! | `AXS_LIBLLAMA_THREADS` | `0` (auto) | CPU threads per context |
//! | `AXS_LIBLLAMA_FLASH_ATTN` | `true` | Enable flash attention |
//! | `AXS_LIBLLAMA_KV_TYPE` | `f16` | KV cache type: `f16`, `q8_0`, `q4_0` |
//! | `AXS_LIBLLAMA_N_BATCH` | `512` | Prompt batch size |
//! | `AXS_LIBLLAMA_POOL_SIZE` | `4` | Max concurrent requests per model |
//!
//! # Architecture
//!
//! Each loaded model gets a `LlamaContextPool` of `pool_size` contexts.
//! `generate()` acquires one context (blocking if all are busy), runs the
//! decode loop on a plain `std::thread`, and releases the context on completion.
//! The model pointer and context pointers are wrapped in safe newtypes that
//! implement `Send`; the model is safe to share (`Sync`) because it is
//! read-only after loading.

